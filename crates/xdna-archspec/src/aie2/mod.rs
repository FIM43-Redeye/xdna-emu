/// Hardware rounding modes for the SRS instruction and bf16 conversion.
///
/// Defines the 10-variant `RoundingMode` enum shared between the integer
/// SRS pipeline (`vector_srs.rs`) and the bf16 conversion path
/// (`vector_float.rs`). Both files previously carried identical local copies.
pub mod rounding;

/// Permute and shuffle data for the AIE2 vector unit.
///
/// `ShuffleMode` (48 hardware shuffle modes) + `SHUFFLE_ROUTING` (hardware-probed
/// byte-routing table, 48 x 64 bytes) drive the `vshuffle` instruction.
/// `MacPermuteMode` (26 modes) + `MacPermuteConfig` + `mac_permute_config()`
/// drive MAC permutation routing. All pure data; execution algorithms
/// (`shuffle_vectors`, `rc2i`) stay in xdna-emu.
pub mod permute;

/// Matrix multiply geometry tables for the AIE2 vector multiply array.
///
/// `DENSE_GEOMETRY_TABLE`, `SPARSE_GEOMETRY_TABLE`, and `CONFIG_GEOMETRY_TABLE`
/// encode the valid tile dimensions (rows, inner, cols) and accumulator modes
/// for each element type pair. Authoritative source for `vector_config.rs`.
pub mod matmul;

/// UPS (Upshift / type-widening) mode table for the AIE2 vector unit.
///
/// The four valid UPS modes: `UpsScale` x `UpsAccMode` -> `UpsMode`
/// (lanes, bits_in, bits_out). Authoritative source for `vector_ups.rs`.
pub mod ups;

/// VMAC crossbar routing data (PRMX + PRMY tables) for the AIE2
/// multiply-accumulate pipeline.
///
/// Pure hardware-probed data (789 active m-bits, 15808 route entries)
/// with two `pub fn` entry points, `eval_prmx` and `eval_prmy`, that
/// operate on the tables. Consumed by `src/interpreter/execute/vmac_hw.rs`
/// via `use xdna_archspec::aie2::vmac::{eval_prmx, eval_prmy};`.
pub mod vmac;

/// Instruction set architecture: decoder tables, runtime model, and
/// LLVM MCDisassembler FFI. Populated during Subsystem 6 Part A.
pub mod isa;

#[cfg(test)]
mod tests {
    use super::instruction_latency;

    /// Drift-detection test for the instruction_latency pipeline constants.
    ///
    /// These constants come from `AIE2Schedule.td` itinerary data and are
    /// cross-validated against xdna-emu's `test_latency_cross_validation_against_itineraries`
    /// test in `src/interpreter/timing/latency.rs`. This standalone archspec test
    /// catches drift without requiring the full emulator crate to compile.
    ///
    /// If any of these values change, verify the new value against
    /// `AIE2Schedule.td` `operand_cycles[0]` for the corresponding itinerary
    /// class before updating.
    #[test]
    fn instruction_latency_constants_match_aie2schedule_td() {
        // Scalar pipeline latencies
        assert_eq!(instruction_latency::SCALAR_MUL, 2,
            "II_MUL operand_cycles[0] = 2 per AIE2Schedule.td");
        assert_eq!(instruction_latency::SCALAR_DIV, 6,
            "6-cycle iterative division per AM020 Ch4 + hardware observation");

        // Vector pipeline latencies
        assert_eq!(instruction_latency::VECTOR_SIMPLE, 2,
            "II_VADD/II_VSUB operand_cycles[0] = 2 per AIE2Schedule.td");
        assert_eq!(instruction_latency::VECTOR_MUL, 5,
            "II_VMUL operand_cycles[0] = 5 per AIE2Schedule.td");
        assert_eq!(instruction_latency::VECTOR_MAC, 5,
            "II_VMAC operand_cycles[0] = 5 per AIE2Schedule.td");
        assert_eq!(instruction_latency::VECTOR_SHUFFLE, 2,
            "II_VSHUFFLE operand_cycles[0] = 2 per AIE2Schedule.td");
        assert_eq!(instruction_latency::VECTOR_PACK, 2,
            "II_VPACK operand_cycles[0] = 2 per AIE2Schedule.td");
    }
}
