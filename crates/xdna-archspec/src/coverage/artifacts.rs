/// Render the perishable queue (spec Section 1) as deterministic markdown.
pub fn render_perishable(arch: Architecture) -> String {
    let m = CoverageModel::build(arch);
    let mut lines = vec![
        format!("# Perishable queue ({arch})"),
        String::new(),
        "Generated by `cargo run -p xdna-archspec --example gen_coverage_artifacts`. Do not hand-edit."
            .to_string(),
        String::new(),
        "Modeled, weak provenance, silicon-unverified (spec Section 1). Empty == this".to_string(),
        "axis of the per-silicon release gate is green for this arch.".to_string(),
        String::new(),
    ];
    let mut cats: Vec<String> = m
        .perishable_queue()
        .iter()
        .map(|v| match v.provenance {
            Provenance::AietoolsModeled => "aietools-modeled (e.g. vector compute)".to_string(),
            Provenance::DocSpecified => "doc-specified (e.g. DMA/stream side effects)".to_string(),
            // Dead arm in Phase 1: perishable_queue() pre-filters to
            // is_perishable() (only AietoolsModeled|DocSpecified).
            // ToolchainDerived, Unspecified, and HardwareObserved cannot
            // reach here. Kept so the match stays total if a future
            // perishable provenance lands.
            _ => "modeled".to_string(),
        })
        .collect();
    cats.sort();
    cats.dedup();
    if cats.is_empty() {
        lines.push("_empty_".to_string());
    } else {
        for c in cats {
            lines.push(format!("- {c}: UNVERIFIED"));
        }
    }
    lines.push(String::new());
    format!("{}\n", lines.join("\n"))
}

/// Render the comprehension gaps (spec Section 1) as deterministic markdown.
pub fn render_comprehension(arch: Architecture) -> String {
    let m = CoverageModel::build(arch);
    let n = m.comprehension_gaps().len();
    let mut lines = vec![
        format!("# Comprehension gaps ({arch})"),
        String::new(),
        "Generated by `cargo run -p xdna-archspec --example gen_coverage_artifacts`. Do not hand-edit."
            .to_string(),
        String::new(),
        "A trusted source describes it and we assert NO model (spec Section 1).".to_string(),
        "Closed by understanding it or explicit Accepted -- never by aging out.".to_string(),
        String::new(),
    ];
    if n == 0 {
        lines.push("_empty_".to_string());
    } else {
        // Phase-1 simplification: at bootstrap the only comprehension-gap
        // source is Intrinsic -> NeedsTriage, so one labeled bullet + count is
        // honest. Plan 2 iterates per-variant -- a future non-Intrinsic
        // Unspecified would otherwise be mislabeled here; the staleness gate
        // forces a regen that surfaces it.
        lines.push(format!("- target-specific intrinsics (SemanticOp::Intrinsic): {n} node(s) UNSPECIFIED"));
    }
    lines.push(String::new());
    format!("{}\n", lines.join("\n"))
}

/// Render the per-arch coverage matrix (spec Section 2 item 2, Section 7).
/// Replaces the hand-maintained docs/coverage/architecture-index.md. The
/// renderer owns the WHOLE file -- fixed preamble + mechanical matrix -- so
/// there is no hand-maintained prose to drift against stale data again (the
/// original failure mode, spec Problem statement).
pub fn render_architecture_index(arch: Architecture) -> String {
    let m = CoverageModel::build(arch);
    let mut lines = vec![
        format!("# {arch} architecture coverage index"),
        String::new(),
        "Generated by `cargo run -p xdna-archspec --example gen_coverage_artifacts`. Do not hand-edit."
            .to_string(),
        String::new(),
        "Mechanical Axis-2 coverage matrix: every SemanticOp category and its".to_string(),
        "rolled-up provenance/verification verdict. This file is generated from".to_string(),
        "the same CoverageModel that backs the release gate, so it cannot".to_string(),
        "silently disagree with `clean_release` (spec Section 2, Section 5).".to_string(),
        "Per-op overrides and the perishable/comprehension queues live in the".to_string(),
        "sibling perishable-queue.md / comprehension-gaps.md.".to_string(),
        "Each category lists the spine domains it tags; per-subsystem detail".to_string(),
        "is in the generated subsystem-index.md.".to_string(),
        String::new(),
        "| Category | Provenance | Verification | Domains |".to_string(),
        "|----------|------------|--------------|---------|".to_string(),
    ];
    // One representative op per category, in a fixed order for determinism.
    let reps: &[(Category, SemanticOp)] = &[
        (Category::Arithmetic, SemanticOp::Add),
        (Category::Bitwise, SemanticOp::And),
        (Category::Comparison, SemanticOp::SetLt),
        (Category::Memory, SemanticOp::Load),
        (Category::ControlFlow, SemanticOp::Br),
        (Category::Vector, SemanticOp::Mac),
        (Category::Sync, SemanticOp::LockAcquire),
        (Category::SideEffect, SemanticOp::DmaStart),
        (Category::NeedsTriage, SemanticOp::Intrinsic(0)),
    ];
    for (cat, rep) in reps {
        debug_assert_eq!(category(rep), *cat, "index representative drifted from category()");
        let v = m.semantic_verdict(rep);
        let doms = category_domains(*cat);
        lines.push(format!(
            "| {cat:?} | {:?} | {:?} | {} |",
            v.provenance,
            v.verification,
            if doms.is_empty() {
                "-".to_string()
            } else {
                doms.join(", ")
            }
        ));
    }
    lines.push(String::new());
    format!("{}\n", lines.join("\n"))
}

/// Render the per-subsystem coverage index (spec Section 4): the retired
/// hand-maintained index's per-subsystem rows, regenerated.
pub fn render_subsystem_index(arch: Architecture) -> String {
    let mut lines = vec![
        format!("# {arch} subsystem coverage index"),
        String::new(),
        "Generated by `cargo run -p xdna-archspec --example gen_coverage_artifacts`. Do not hand-edit."
            .to_string(),
        String::new(),
        "Per-subsystem rows recovered from the retired hand-maintained index as".to_string(),
        "generated data (spec 2026-05-16-subsystem-axis-coverage-enrichment).".to_string(),
        "`rolled-up` is the worst-wins verdict of the categories tagged to the".to_string(),
        "domain, or `-` for category-orphan domains. Sibling queues:".to_string(),
        "implementation-gaps.md / perishable-queue.md / comprehension-gaps.md.".to_string(),
        String::new(),
        "| Domain | Source | Our location | Own verdict | Rolled-up | Drift | Narrative |".to_string(),
        "|--------|--------|--------------|-------------|-----------|-------|-----------|".to_string(),
    ];
    for d in capability_spine().into_iter().filter(|d| d.applies_to(arch)) {
        let rolled = match rolled_up_verdict(&d.id) {
            Some(v) => format!("{:?}/{:?}", v.provenance, v.verification),
            None => "-".to_string(),
        };
        let drift = if is_category_orphan(&d.id) {
            "orphan".to_string()
        } else if unannotated_material_drift(&d.id) {
            "MATERIAL".to_string()
        } else if d.drift_rationale.is_some() {
            "annotated".to_string()
        } else {
            "ok".to_string()
        };
        let src = d.source_ref.replace('|', "\\|");
        let locs_cell = if d.src_locations.is_empty() {
            "-".to_string()
        } else {
            d.src_locations.join(", ").replace('|', "\\|")
        };
        lines.push(format!(
            "| {} | {} | {} | {:?}/{:?} | {} | {} | {} |",
            d.id,
            src,
            locs_cell,
            d.verdict.provenance,
            d.verdict.verification,
            rolled,
            drift,
            d.narrative.replace('|', "\\|").replace('\n', " ")
        ));
    }
    lines.push(String::new());
    format!("{}\n", lines.join("\n"))
}

/// Render the implementation-gap queue (spec Section 4): domains whose own
/// seeded verdict is a `Modeled{Partial|Stub}` (or explicit MISSING). Sourced
/// from `capability_spine()` domain verdicts -- NEVER a category rollup, which
/// can never be `Modeled` (a permanently empty queue otherwise -- spec S1/M1).
pub fn render_implementation_gaps(arch: Architecture) -> String {
    let mut lines = vec![
        format!("# Implementation gaps ({arch})"),
        String::new(),
        "Generated by `cargo run -p xdna-archspec --example gen_coverage_artifacts`. Do not hand-edit."
            .to_string(),
        String::new(),
        "Subsystems only partially built or stubbed (spec Section 1, third gap".to_string(),
        "class). Empty == every named subsystem is Full/closed for this arch.".to_string(),
        String::new(),
    ];
    let gaps: Vec<_> = capability_spine()
        .into_iter()
        .filter(|d| d.applies_to(arch) && d.verdict.is_implementation_gap())
        .collect();
    if gaps.is_empty() {
        lines.push("_empty_".to_string());
    } else {
        for d in gaps {
            let what = match &d.verdict.verification {
                Verification::Modeled { completeness: Completeness::Partial { missing } } => {
                    format!("PARTIAL -- missing: {missing}")
                }
                Verification::Modeled { completeness: Completeness::Stub } => "STUB".to_string(),
                Verification::Modeled { completeness: Completeness::Full } => {
                    unreachable!("is_implementation_gap() filter should have excluded Modeled/Full")
                }
                Verification::NotApplicable
                | Verification::Verified { .. }
                | Verification::Unverified
                | Verification::Accepted { .. } => {
                    unreachable!(
                        "is_implementation_gap() filter should have excluded non-Modeled verifications"
                    )
                }
            };
            lines.push(format!("- {}: {what}", d.id));
        }
    }
    lines.push(String::new());
    format!("{}\n", lines.join("\n"))
}

